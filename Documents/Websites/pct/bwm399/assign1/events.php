<h1>2013–14 Events</h1>

<h3>The Essex Consort: A Celebration of St. Crispin’s Day
<em>September 14, 2013 6PM</em></h3>

<p>The Essex Consort is one of the leading ensembles specializing in the performance of Medieval and Renaissance music. Drawing on a repertoire of several centuries, the group has long been an audience favorite in its home city of New York, and annual tours include sold-out performances at major concert halls coast to coast. In the spirit and pageantry of medieval church dramas and mystery plays, eight singers and five instrumentalists playing reproductions of medieval instruments recount the events of St Crispin's Day—a program combining solemn and festive music, processions and simple gestures to dramatize the narrative as conveyed by music manuscripts and illuminated miniatures of the Middle Ages.<br> <br>
<strong>Tickets: Adults, $15; seniors, $10; students, $5 (with ID)</strong></p>

<h3>Midland City:
<em>October 25, 2013 6PM</em></h3>

<p>Midland City, with lead guitarist Philip Castle, breathes new life into the all-string instrumentation of violin, bass and guitars. Hearing the ensemble live carries the listener back to the 1930s, to the small, smoky jazz clubs of Paris or the refined lounges of the famous Hotel Ritz. For this performance, Midland City presents Seed Crystal, an evening of Acid Jazz and instrumentation inspired by the Paris café scene. A wine tasting featuring spirits from local wineries and microbrews will follow the event, Proof of age required.  <br> <br>
<strong>Tickets: Adults, $25; seniors, $10; students, $10 (with ID)</strong></p>

<h3>En Pointe:
<em>November 24, 2013 7:30PM</em></h3>

<p>Celebrating its 25th anniversary this year, En Pointe American Dance Theater has earned a reputation as one of the most acclaimed troupes for modern dance. En Pointe, is universally renowned for merging the spirit and energy of the country’s best young dance talent with the passion and creative vision of today’s most outstanding emerging choreographers. En Pointe’s Artistic Director, George Bergeron, appointed by the troupes founder in 1994, has established this ensemble as one of the most popular dance companies in the country.<br> <br>
<strong>Adults, $20; seniors, $15; students, $5 (with ID)</strong></p>

<h3>J. Edgar Nation:
<em>December 3, 2013 7:30PM</em></h3>

<p>J. Edgar Nation is the author of four collections of poetry, most recently Kazak (Titan Publishing, 2009). His first collection, Victim of a Series of Accidents, won the New England Regional Poetry prize. He is co-editor of American Poets in the Twenty-First Century and will be reading new work from an upcoming untitled collection.<br> <br>
There will be a book signing immediately following the reading.<br> 
<strong>Adults, $10; seniors, $5; students, $5 (with ID)</strong></p>

<h3>William Shakespeare’s Macbeth: Performed by the Ironbound Players
<em>December 30, 2013 6:00PM</em></h3>

<p>Shakespeare’s swift and relentless tragedy uncovers the terrifying consequences of blind ambition. In medieval Scotland, a savage world of ghosts, witches and bloody battlefields, a dark prophesy leads a warrior and his wife past the point of no return. 
The Ironbound Players are one of New Jersey’s most celebrated acting troupes having won awards from various arts organizations they were called by the Star Ledger, “one of the best that Jersey has to offer”.<br> <br>
<strong>Adults, $20; seniors, $15; students, $15 (with ID)</strong></p>

<h3>Hazel Crosby:
<em>February 17, 2014 7:00PM</em></h3>

<p>Award-winning artist, Hazel Crosby established her career as a compelling singer/songwriter with the debut releases Busy, Busy, Busy and Ice10, launching her the smash hit single Illium at Midnight. Through the 1980s and 90s Crosby alternated recording with acting, appearing in the film Breakfast of Champions, on the television series Kurt Vonnegut's Monkey House, and co-writing and starring in the musical The Republic of San Lorenzo.<br> <br>
<strong>Adults, $25; seniors, $15; students, $10 (with ID)</strong></p>

<h3>Arthur Barnhouse: “Forces of the Mind”
<em>March 1, 2014 8:30PM</em> </h3>

<p>Arthur Barnhouse, Grammy Award-winning stand-up comedian and satirist, is one of the most prolific and popular performers working today. His live performances provide a cathartic release of anger and disillusionment for his audience. He is a passionate performer who is more angry-optimist than mean-spirited curmudgeon. Barnhouse is the rare comic who can cause an audience to laugh themselves into incontinence while making compelling points about the absurdity of our world.<br> <br>
Content may not be suitable for younger audiences
<strong>Adults, $20; seniors, $15; students, $10 (with ID)</strong></p>

<h3>Magic Lantern Marionette Presents “Mother Goose on the Loose”
<em>April 25, 2014 1:30PM & 4:00PM</em></h3>

<p>Magic Lantern Marionette invites families with young children to a very special presentation of "Mother Goose on the Loose". This puppet show has been created especially for the littlest ones, ages 2–6, but all ages may come and enjoy this simple but beautiful retelling of classic nursery rhymes and stories.<br> <br>
<strong>Adults, $5; seniors, $5; Children 6 and younger, free</strong></p>

<h3>Papa Monzano Flamenco Dance Company: Flamenco Festival 2014:
<em>June 16, 2014 2:00PM</em></h3>

<p>Flamenco Festival 2014 returns for a glorious 12th season of world-class flamenco dance and music. Exceptional flamenco dancers and a sensational band of musicians and virtuoso singers come together in this production from legendary flamenco guitarist "Papa" Monzano. Packed with intensity, depth and raw energy, Flamenco Festival 2014 captures all the passions of present-day flamenco.<br> <br>
<strong>Adults, $25; seniors, $10; students, $10 (with ID)</strong></p>

<h3>Felix Hoenikker Pesents: The Roaring Twenties Music Review
<em>July 6, 2014 5:00PM</em></h3>

<p>The World Famous Felix Hoenikker is one of the most popular and sought after band leaders in the world today for both concert and swing dance engagements. With his unique jazz sound, Hoenikker takes audiences back to the roaring twenties and the time of flappers and speakeasies. Join us for this musical memory tour that includes comedy, nostalgia, and a night of pure joy. <br> <br>
<strong>Adults, $10; seniors, $5; students, $5 (with ID)</strong></p>
